import {
  comparisonEntries,
  officialComparisonEntries,
  type CatalogueSource,
  type ComparisonEntry,
  type ComparisonSlug,
} from "./comparison-manifest";

export type VisualStateKind = "static" | "overlay" | "interaction" | "keyboard" | "visual";
export type VisualStateSideStatus = "visual" | "asserted" | "planned" | "missing" | "na";
export type PairDiffStatus = "strict" | "asserted" | "planned" | "blocked" | "na";

export interface VisualStateTarget {
  id: string;
  label: string;
  kind: VisualStateKind;
  react: VisualStateSideStatus;
  solid: VisualStateSideStatus;
  pairDiff: PairDiffStatus;
  spec?: string;
  note: string;
}

export interface VisualStateCoverage {
  slug: ComparisonSlug;
  title: string;
  source: CatalogueSource;
  states: readonly VisualStateTarget[];
}

function plannedState(entry: ComparisonEntry): VisualStateTarget {
  const reactLive = entry.layers.styled.react === "live";
  const solidLive = entry.layers.styled.solid === "live";

  return {
    id: "styled.default",
    label: "Styled default",
    kind: "static",
    react: reactLive ? "planned" : "missing",
    solid: solidLive ? "planned" : "missing",
    pairDiff: reactLive && solidLive ? "planned" : "blocked",
    note:
      reactLive && solidLive
        ? "Route is live, but current visual evidence and strict pair diff are still missing."
        : "Blocked until both the exact React Spectrum reference and Solid styled implementation are live.",
  };
}

function visualDefaultState(input: {
  slug: string;
  label?: string;
  note?: string;
  pairDiff?: PairDiffStatus;
  spec?: string;
}): VisualStateTarget {
  const label = input.label ?? "Styled default";
  const pairDiff = input.pairDiff ?? "asserted";

  return {
    id: "styled.default",
    label,
    kind: "static",
    react: "visual",
    solid: "visual",
    pairDiff,
    spec: input.spec ?? "e2e/default-state-visual.spec.ts + e2e/default-state-pair-diff.spec.ts",
    note:
      input.note ??
      (pairDiff === "strict"
        ? "Current default-state React/Solid captures are compared with zero pixel tolerance."
        : "Current default-state React/Solid captures are compared with an explicit asserted pair-diff threshold."),
  };
}

function assertedDefaultState(input: {
  slug: string;
  label?: string;
  note: string;
}): VisualStateTarget {
  const label = input.label ?? "Styled default";

  return {
    id: "styled.default",
    label,
    kind: "static",
    react: "visual",
    solid: "visual",
    pairDiff: "asserted",
    spec: "e2e/live-styled-visual.spec.ts",
    note: input.note,
  };
}

